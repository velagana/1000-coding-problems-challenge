percentage = int(input("Enter the percentage :"))
if percentage in range  (90 - 100):
    print("A grade")
elif percentage in range  (80 -90):
    print("B grade") 
elif percentage in range  (70 - 80):
    print("C grade") 
elif percentage in range  (60 - 70):
    print("D grade") 
elif percentage in range  (50 - 60):
    print("E grade")
else:
    print("F grade")
   